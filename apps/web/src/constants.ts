export type LanguageDirection = 'ltr' | 'rtl';

type LanguageMeta = {
    label: string;
    nativeLabel: string;
    dir: LanguageDirection;
    locale: string;
    aiName: string;
};

export const DEFAULT_LANGUAGE: Language = 'id';

