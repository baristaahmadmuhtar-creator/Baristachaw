const explicitSourceUrls = new Map();

function rangeForRawFine(entry) {
  return entry.espresso || entry.moka || entry.aeropress || 'Manual setting required';
}

function buildExpertDescription(entry) {
  const geometry = entry.burrType === 'flat'
    ? 'flat burr clarity/flow platform'
    : entry.burrType === 'ceramic'
      ? 'ceramic entry grinder platform'
      : 'conical burr body/forgiveness platform';
  return [
    `${entry.name} calibrated from the ${MASTER_VERSION} master grinder table.`,
    `Mechanical cue: ${geometry}; ${entry.type}.`,
    'Iced filter settings are intentionally finer than hot equivalents to compensate for lower active hot-water solvent and shorter contact time.',
    'Treat all numbers as true-zero starting ranges; burr seasoning, roast density, RDT, dose, paper/filter behavior, flow, brew time, and taste still decide the final setting.',
  ].join(' ');
}

