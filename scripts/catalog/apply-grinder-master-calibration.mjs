function buildSetting(entry, suffix, rangeKey, brewMode, label, profileIds) {
  const rangeLabel = entry[rangeKey];
  if (!rangeLabel || profileIds.length === 0) return null;
  const grinderId = slugify(entry.name);
  return {
    id: `gs_master_${grinderId}_${suffix}`,
    grinderId,
    brewMode,
    profileIds,
    rangeLabel,
    note: [
      `${label} range from ${MASTER_VERSION}.`,
      brewMode === 'iced'
        ? 'Iced range is set finer than the hot baseline to offset reduced active hot-water solvent and stabilize extraction over ice.'
        : 'Use as a true-zero starting point, then adjust by taste, flow, and brew time.',
      'Burr gap is not a linear particle-size conversion; verify zero point, burr seasoning, roast density, dose, and paper/filter behavior.',
    ].join(' '),
    referenceType: 'method_specific_master_table',
    zeroPointMethod: 'true zero / burr-touch reference; user grinder may need offset calibration',
    calibrationRequired: true,
    source: 'curated_2026_master_calibration',
    sourceUrls: uniq([...(entry.sourceUrls || []), ...defaultMasterSourceUrls]),
    verificationLevel: 'curated',
    verifiedAt: UPDATED_AT,
    popularityTier: entry.popularityTier || 'specialty_common',
    marketSegment: entry.marketSegment || 'specialty_mainstream',
    releaseStatus: entry.releaseStatus || 'established',
    confidence: 'medium',
    catalogVersion: MASTER_VERSION,
  };
}

function buildSettings(profiles) {
  const groups = buildProfileGroups(profiles);
  const settings = [];
  for (const entry of master) {
    for (const [suffix, rangeKey, brewMode, label] of settingSpecs) {
      const setting = buildSetting(entry, suffix, rangeKey, brewMode, label, groups[suffix] || []);
      if (setting) settings.push(setting);
    }
  }
  return settings;
}

function modelFromName(entry) {
  const brandPattern = new RegExp(`^${entry.brand.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\s+`, 'i');
  return entry.name.replace(brandPattern, '').trim();
}

function grinderSearchText(entry) {
  return uniq([
    entry.name,
    entry.brand,
    modelFromName(entry),
    entry.type,
    ...(entry.aliases || []),
    entry.espresso ? `espresso ${entry.espresso}` : 'espresso not recommended',
    `moka ${entry.moka}`,
    `aeropress ${entry.aeropress}`,
    `v60 origami kono ${entry.coneHot}`,
    `ice brew finer ${entry.coneIced}`,
    `kalita april melitta ${entry.flatHot}`,
    `chemex ${entry.chemexHot}`,
    `french press cold brew batch ${entry.coarse}`,
  ]).join(' ').toLowerCase();
}

function buildNormalizedGrinder(entry, index) {
  const id = slugify(entry.name);
  const urls = uniq([...(entry.sourceUrls || []), ...defaultMasterSourceUrls]);
  const sources = urls.map((url, sourceIndex) => source(url, sourceIndex === 0 ? 'community_reference' : 'catalog_seed', sourceIndex === 0 ? 0.78 : 0.7));
  return {
    id,
    published: true,
    publish_state: 'published',
    search_text: grinderSearchText(entry),
    aliases: uniq([entry.name, ...(entry.aliases || [])]),
    available_in: ['Indonesia', 'Brunei', 'Singapore', 'Malaysia', 'Global'],
    verification_status: 'curated',
    data_quality: {
      is_estimated: false,
      missing_fields: [],
      completeness_score: 95,
    },
    brand: entry.brand,
    model: modelFromName(entry),
    grinder_type: entry.grinderType || null,
    burr_type: entry.burrType === 'ceramic' ? 'conical' : entry.burrType || null,
    burr_material: entry.burrType === 'ceramic' ? 'ceramic' : entry.burrMaterial || 'steel',
    burr_size_mm: entry.burrSizeMm || null,
    step_type: /stepless/i.test(entry.type) ? 'stepless' : 'stepped',
    recommended_range: {
      espresso: entry.espresso,
      pour_over: entry.coneHot,
      french_press: entry.coarse,
    },
    retention_notes: 'Master table reference only. Calibrate true zero, burr seasoning, RDT/static behavior, roast density, flow, brew time, and taste before treating the setting as exact.',
    sources,
    primary_source: sources[0],
    source_type: 'community_reference',
    confidence_score: 0.78,
    manual_brew_capable: true,
    filter_priority: 1000 - index,
    availability_confidence: 'medium',
    updated_at: UPDATED_AT,
  };
}

