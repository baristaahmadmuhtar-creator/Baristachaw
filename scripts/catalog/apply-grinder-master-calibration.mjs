async function readJson(relativePath) {
  return JSON.parse(await fs.readFile(file(relativePath), 'utf8'));
}

async function writeJson(relativePath, data) {
  await fs.mkdir(path.dirname(file(relativePath)), { recursive: true });
  await fs.writeFile(file(relativePath), `${JSON.stringify(data, null, 2)}\n`, 'utf8');
}

async function main() {
  const profilesFile = await readJson('apps/web/public/data/ai-brew/device-brew-profiles.v2026-06.json');
  const profiles = readItems(profilesFile);
  const publicGrinders = master.map(buildPublicGrinder);
  const settings = buildSettings(profiles);
  const normalized = master.map(buildNormalizedGrinder);
  const search = normalized.map(toEquipmentSearch);

  const normalizedFile = {
    version: MASTER_VERSION,
    items: normalized,
  };
  const searchFile = {
    version: MASTER_VERSION,
    items: search,
  };
  const meta = await readJson('data/catalog/exports/phase1/meta.json').catch(() => ({ counts: {} }));
  const nextMeta = {
    ...meta,
    version: MASTER_VERSION,
    generated_at: new Date().toISOString(),
    counts: {
      ...(meta.counts || {}),
      grinders_total: normalized.length,
      grinders_published: search.length,
    },
  };

  await Promise.all([
    writeJson('apps/web/public/data/ai-brew/grinders.v2026-03.json', {
      catalogVersion: MASTER_VERSION,
      items: publicGrinders,
    }),
    writeJson('apps/web/public/data/ai-brew/grinder-settings.v2026-06.json', {
      catalogVersion: MASTER_VERSION,
      items: settings,
    }),
    writeJson('data/catalog/normalized/phase1/grinders.json', normalizedFile),
    writeJson('data/catalog/exports/phase1/grinders.search.json', searchFile),
    writeJson('apps/web/public/data/catalog/phase1/grinders.search.json', searchFile),
    writeJson('data/catalog/exports/phase1/meta.json', nextMeta),
    writeJson('apps/web/public/data/catalog/phase1/meta.json', nextMeta),
  ]);

  console.log(`Applied ${MASTER_VERSION}: ${publicGrinders.length} grinders, ${settings.length} method-specific settings.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
